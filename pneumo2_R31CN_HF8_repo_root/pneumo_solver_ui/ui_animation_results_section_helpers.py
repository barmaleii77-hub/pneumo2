from __future__ import annotations

from pathlib import Path
from typing import Any, Callable

from pneumo_solver_ui.ui_animation_mode_helpers import render_animation_results_section
from pneumo_solver_ui.ui_animation_results_builders import (
    build_app_mechanical_animation_panel_kwargs,
    build_flow_animation_panel_kwargs,
    build_heavy_mechanical_animation_panel_kwargs,
    build_svg_scheme_animation_surface_kwargs,
)


def _render_built_animation_results_section(
    st: Any,
    *,
    cur_hash: str,
    test_pick: str,
    render_mechanics_panel_fn: Callable[..., None],
    mechanics_panel_kwargs: dict[str, Any],
    render_flow_tool_panel_fn: Callable[..., None],
    render_svg_scheme_section_fn: Callable[..., None],
    session_state: Any,
    df_mdot: Any,
    df_open: Any,
    df_p: Any,
    base_dir: Path,
    default_svg_mapping_path: Any,
    route_write_view_box: str,
    do_rerun_fn: Any,
    log_event_fn: Any,
    p_atm: float,
    model_module: Any,
    pressure_divisor: float,
    pressure_unit: str,
    dataset_id: Any,
    safe_dataframe_fn: Any,
    flow_scale_and_unit_fn: Any,
    get_svg_component_fn: Any,
    render_svg_flow_animation_html_fn: Any,
    has_svg_autotrace: bool,
    extract_polylines_fn: Any,
    auto_build_mapping_from_svg_fn: Any,
    detect_component_bboxes_fn: Any,
    name_score_fn: Any,
    shortest_path_fn: Any,
    evaluate_quality_fn: Any,
    render_flow_panel_html_fn: Any,
) -> str:
    flow_panel_kwargs = build_flow_animation_panel_kwargs(
        df_mdot=df_mdot,
        df_open=df_open,
        p_atm=p_atm,
        model_module=model_module,
        flow_scale_and_unit_fn=flow_scale_and_unit_fn,
        render_flow_panel_html_fn=render_flow_panel_html_fn,
    )
    return render_animation_results_section(
        st,
        cur_hash=cur_hash,
        test_pick=test_pick,
        render_mechanics_panel_fn=render_mechanics_panel_fn,
        mechanics_panel_kwargs=mechanics_panel_kwargs,
        render_flow_tool_panel_fn=render_flow_tool_panel_fn,
        flow_panel_kwargs=flow_panel_kwargs,
        render_svg_scheme_section_fn=render_svg_scheme_section_fn,
        **build_svg_scheme_animation_surface_kwargs(
            session_state,
            df_mdot=df_mdot,
            df_open=df_open,
            df_p=df_p,
            base_dir=base_dir,
            default_svg_mapping_path=default_svg_mapping_path,
            route_write_view_box=route_write_view_box,
            do_rerun_fn=do_rerun_fn,
            log_event_fn=log_event_fn,
            p_atm=p_atm,
            model_module=model_module,
            pressure_divisor=pressure_divisor,
            pressure_unit=pressure_unit,
            dataset_id=dataset_id,
            safe_dataframe_fn=safe_dataframe_fn,
            flow_scale_and_unit_fn=flow_scale_and_unit_fn,
            get_component_fn=get_svg_component_fn,
            render_svg_flow_animation_html_fn=render_svg_flow_animation_html_fn,
            has_svg_autotrace=has_svg_autotrace,
            extract_polylines_fn=extract_polylines_fn,
            auto_build_mapping_from_svg_fn=auto_build_mapping_from_svg_fn,
            detect_component_bboxes_fn=detect_component_bboxes_fn,
            name_score_fn=name_score_fn,
            shortest_path_fn=shortest_path_fn,
            evaluate_quality_fn=evaluate_quality_fn,
        ),
    )


