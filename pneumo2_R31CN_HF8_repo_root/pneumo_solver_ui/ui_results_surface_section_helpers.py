def _build_results_playhead_section_kwargs(
    *,
    st: Any,
    dataset_id: str,
    time_s: Sequence[float] | None,
    session_state: Any,
    events_list: Sequence[dict[str, Any]] | None,
    safe_dataframe_fn: Any,
    df_main: Any,
    df_p: Any,
    df_mdot: Any,
    playhead_x: float | None,
    pressure_from_pa_fn: Callable[[object], float],
    pressure_unit: str,
    stroke_scale: float,
    stroke_unit: str,
    flow_scale_and_unit_fn: Any,
    p_atm: float,
    model_module: Any,
    missing_component_fallback_fn: Callable[[], None] | None = None,
) -> dict[str, Any]:
    kwargs: dict[str, Any] = {
        "playhead_component": get_playhead_ctrl_component(),
        "dataset_id": dataset_id,
        "time_s": time_s,
        "session_state": session_state,
        "events_list": events_list,
        "safe_dataframe_fn": safe_dataframe_fn,
        "df_main": df_main,
        "df_p": df_p,
        "df_mdot": df_mdot,
        "playhead_x": playhead_x,
        "pressure_from_pa_fn": pressure_from_pa_fn,
        "pressure_unit": pressure_unit,
        "stroke_scale": stroke_scale,
        "stroke_unit": stroke_unit,
        "flow_scale_and_unit_fn": flow_scale_and_unit_fn,
        "p_atm": p_atm,
        "model_module": model_module,
        "info_fn": st.info,
        "caption_fn": st.caption,
        "expander_fn": st.expander,
        "columns_fn": st.columns,
        "checkbox_fn": st.checkbox,
    }
    if missing_component_fallback_fn is not None:
        kwargs["missing_component_fallback_fn"] = missing_component_fallback_fn
    return kwargs


