        # Массивы камер (учёт разных ходов Ц1/Ц2 и перед/зад)
    V0_vec = np.array([n.V0 for n in nodes], dtype=float)
    chamber_indices = []
    chamber_corner = []
    chamber_cyl_id = []   # 1 для C1, 2 для C2
    chamber_sign = []
