        s_for_ch = np.where(chamber_cyl_id == 1, s_C1[chamber_corner], s_C2[chamber_corner])
        sdot_for_ch = np.where(chamber_cyl_id == 1, s_dot_C1[chamber_corner], s_dot_C2[chamber_corner])

        V[chamber_indices] = chamber_offset + chamber_sign*chamber_area*s_for_ch
        dV[chamber_indices] = chamber_sign*chamber_area*sdot_for_ch

