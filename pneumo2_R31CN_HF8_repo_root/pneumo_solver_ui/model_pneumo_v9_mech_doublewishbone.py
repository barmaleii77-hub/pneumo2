    #  - 'DELTA': как в базовой версии (x = x0 + delta) — совместимость
    #  - 'C1'   : пружина коаксиальна Ц1, работает по ходу штока (x = x0 + (s0_C1 - s_C1))
    #  - 'C2'   : аналогично по Ц2
    spring_mode = _pstr('пружина_по_цилиндру', 'C1').strip().upper()
    if spring_mode not in ('DELTA', 'C1', 'C2'):
        spring_mode = 'C1'

