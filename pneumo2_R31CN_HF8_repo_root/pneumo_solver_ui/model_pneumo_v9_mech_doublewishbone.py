    F_cyl_vert0 = np.zeros(4)
    for i_c, cname in enumerate(corner_order):
        p1c = p0[node_index[f'Ц1_{cname}_БП']]
        p1r = p0[node_index[f'Ц1_{cname}_ШП']]
        p2c = p0[node_index[f'Ц2_{cname}_БП']]
        p2r = p0[node_index[f'Ц2_{cname}_ШП']]
        F1_ax = (p1c - P_ATM) * A1_cap - (p1r - P_ATM) * A1_rod
        F2_ax = (p2c - P_ATM) * A2_cap - (p2r - P_ATM) * A2_rod
