    if mech_kin == 'legacy':
        ds_ddelta0_C1 = -np.ones(4)
        ds_ddelta0_C2 = -np.ones(4)
    else:
        # ds/dδ = dL/dδ = -(low_frac * dz)/L, где dz = top_z - low_frac*delta; при delta=0: dz=top_z, L=L0
        ds_ddelta0_C1 = -(low_frac_C1 * top_z_C1) / np.maximum(1e-9, L0_C1)
        ds_ddelta0_C2 = -(low_frac_C2 * top_z_C2) / np.maximum(1e-9, L0_C2)

