    # Функция объёмов (дифференциальная)
    def volumes(z, phi, theta, zw, z_dot, phi_dot, theta_dot, zw_dot):
        V = V0_vec.copy()
        dV = np.zeros_like(V)

        z_body = z + math.sin(phi)*y_pos*math.cos(theta) - math.sin(theta)*x_pos
        delta = zw - z_body

