            # Проекция масс: не даём уйти в отрицательные массы и удерживаем p_abs >= p_abs_min (допускаем вакуум)
            p_abs_min = float(params.get('минимальное_абсолютное_давление_Па', 1000.0))
            z_, phi_, theta_ = state[0], state[1], state[2]
            zw_ = state[3:7]
