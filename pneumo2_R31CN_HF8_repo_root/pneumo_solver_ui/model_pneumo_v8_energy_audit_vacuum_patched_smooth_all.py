    def _cumtrapz(arr, dt_):
        arr = np.asarray(arr, dtype=float)
        outi = np.zeros_like(arr)
        if len(arr) >= 2:
            outi[1:] = np.cumsum(0.5 * (arr[1:] + arr[:-1]) * dt_)
        return outi

