def _smoothstep01(x: float) -> float:
    """Гладкая стыковка 0→1 (C1), x∈[0,1]."""
    if x <= 0.0:
        return 0.0
    if x >= 1.0:
        return 1.0
    return x * x * (3.0 - 2.0 * x)

def _smoothstep01_grad(x: float) -> float:
    """Производная smoothstep01 по x (внутри (0,1))."""
    if x <= 0.0 or x >= 1.0:
        return 0.0
    return 6.0 * x * (1.0 - x)

def _step_with_ramp(t: float, t0: float, val: float, ramp_s: float) -> float:
    """Ступенька, но с плавным включением за ramp_s секунд."""
    if t <= t0:
        return 0.0
    if ramp_s is None or ramp_s <= 0.0:
        return float(val)
    x = (t - t0) / float(ramp_s)
    if x >= 1.0:
        return float(val)
    return float(val) * _smoothstep01(x)

def _step_with_ramp_dot(t: float, t0: float, val: float, ramp_s: float) -> float:
    """Производная _step_with_ramp по времени t (в зоне плавного фронта)."""
    if t <= t0:
        return 0.0
    if ramp_s is None or ramp_s <= 0.0:
        # идеальная ступенька: производная не определена (δ-импульс), берём 0 для численной устойчивости
        return 0.0
    ramp_s = float(ramp_s)
    x = (t - t0) / ramp_s
    if x <= 0.0 or x >= 1.0:
        return 0.0
    return float(val) * _smoothstep01_grad(x) / ramp_s

def _bump_with_smooth(t: float, t0: float, dur: float, A: float, smooth_frac: float = 0.2) -> float:
    """Прямоугольная кочка A на интервале [t0, t0+dur] с плавными фронтами."""
    if dur is None:
        return 0.0
    dur = float(dur)
    if dur <= 0.0:
        return 0.0
    t1 = float(t0)
    t2 = t1 + dur
    if t < t1 or t > t2:
        return 0.0

    sf = float(smooth_frac) if smooth_frac is not None else 0.0
    sf = max(0.0, min(0.49, sf))
    ramp = max(1e-9, sf * dur)

    # Включение
    if t < t1 + ramp:
        x = (t - t1) / ramp
        return float(A) * _smoothstep01(x)

    # Выключение
    if t > t2 - ramp:
        x = (t2 - t) / ramp
        return float(A) * _smoothstep01(x)

    return float(A)

def _bump_with_smooth_dot(t: float, t0: float, dur: float, A: float, smooth_frac: float = 0.2) -> float:
    """Производная _bump_with_smooth по времени t (в зонах плавных фронтов)."""
    if dur is None:
        return 0.0
    dur = float(dur)
    if dur <= 0.0:
        return 0.0
    t1 = float(t0)
    t2 = t1 + dur
    if t < t1 or t > t2:
        return 0.0

    sf = float(smooth_frac) if smooth_frac is not None else 0.0
    sf = max(0.0, min(0.49, sf))
    ramp = max(1e-9, sf * dur)

    # Включение (t∈[t1, t1+ramp])
    if t < t1 + ramp:
        x = (t - t1) / ramp
        return float(A) * _smoothstep01_grad(x) / ramp

    # Выключение (t∈[t2-ramp, t2])
    if t > t2 - ramp:
        x = (t2 - t) / ramp
        # dx/dt = -1/ramp
        return float(A) * _smoothstep01_grad(x) * (-1.0 / ramp)

    # На плато кочки производная нулевая
    return 0.0

def _compile_suite_test_inputs(test: dict, params: dict):
    """Поддержка 'тип' из suite/UI: строит ax_func/ay_func/road_func если их нет.
    Возвращает dict с функциями (может быть пустым).
    """
    if not isinstance(test, dict):
        return {}
    ttype = str(test.get("тип", "")).strip()
    if not ttype:
        return {}

    # Если пользователь/скрипт уже передал явные функции — не трогаем.
    has_any = any(callable(test.get(k, None)) for k in ("ax_func", "ay_func", "road_func"))
    if has_any:
        return {}

    add = {}

    # Геометрия (для диагональной кочки)
    # ABSOLUTE LAW: canonical keys only ("база" / "колея").
    # Defaults match pneumo_solver_ui/default_base.json.
    L = float(params.get("база", 1.5) or 1.5)      # колесная база
    W = float(params.get("колея", 1.0) or 1.0)     # колея

    # Общие параметры сглаживания
    smooth_frac = float(test.get("доля_плавной_стыковки", 0.2) or 0.2)

    if ttype == "инерция_крен":
        ay = float(test.get("ay_м_с2", test.get("ay", 0.0)) or 0.0)
        t_step = float(test.get("t_step", 0.0) or 0.0)
        ramp = float(test.get("ramp_s", 0.05) or 0.05)
        add["ay_func"] = lambda t, ay=ay, t0=t_step, ramp=ramp: _step_with_ramp(t, t0, ay, ramp)
        add["ax_func"] = lambda t: 0.0
        add["road_func"] = lambda t: np.zeros(4, dtype=float)
        add["road_func_dot"] = lambda t: np.zeros(4, dtype=float)

    elif ttype == "инерция_тангаж":
        ax = float(test.get("ax_м_с2", test.get("ax", 0.0)) or 0.0)
        t_step = float(test.get("t_step", 0.0) or 0.0)
        ramp = float(test.get("ramp_s", 0.05) or 0.05)
        add["ax_func"] = lambda t, ax=ax, t0=t_step, ramp=ramp: _step_with_ramp(t, t0, ax, ramp)
        add["ay_func"] = lambda t: 0.0
        add["road_func"] = lambda t: np.zeros(4, dtype=float)
        add["road_func_dot"] = lambda t: np.zeros(4, dtype=float)

    elif ttype == "микро_синфаза":
        A = float(test.get("A_м", test.get("A", 0.0)) or 0.0)
        f = float(test.get("f_Гц", test.get("f", 1.0)) or 1.0)
        w = 2.0 * math.pi * f
        add["road_func"] = lambda t, A=A, w=w: (A * math.sin(w * t)) * np.ones(4, dtype=float)
        add["road_func_dot"] = lambda t, A=A, w=w: (A * w * math.cos(w * t)) * np.ones(4, dtype=float)
        add["ax_func"] = lambda t: 0.0
        add["ay_func"] = lambda t: 0.0

    elif ttype == "микро_разнофаза":
        A = float(test.get("A_м", test.get("A", 0.0)) or 0.0)
        f = float(test.get("f_Гц", test.get("f", 1.0)) or 1.0)
        w = 2.0 * math.pi * f
        # Левая сторона +, правая - (возбуждаем крен)
        s = np.array([+1.0, -1.0, +1.0, -1.0], dtype=float)
        add["road_func"] = lambda t, A=A, w=w, s=s: (A * math.sin(w * t)) * s
        add["road_func_dot"] = lambda t, A=A, w=w, s=s: (A * w * math.cos(w * t)) * s
        add["ax_func"] = lambda t: 0.0
        add["ay_func"] = lambda t: 0.0

    elif ttype == "кочка_одно_колесо":
        A = float(test.get("A_м", test.get("A", 0.0)) or 0.0)
        t0 = float(test.get("t0", 0.0) or 0.0)
        dur = float(test.get("dur", 0.0) or 0.0)
        wheel = str(test.get("колесо", "ЛП")).strip().upper()
        idx_map = {"ЛП": 0, "ПП": 1, "ЛЗ": 2, "ПЗ": 3}
        i = idx_map.get(wheel, 0)

        def road_func(t, i=i, A=A, t0=t0, dur=dur, sf=smooth_frac):
            v = np.zeros(4, dtype=float)
            v[i] = _bump_with_smooth(t, t0, dur, A, sf)
            return v

        def road_func_dot(t, i=i, A=A, t0=t0, dur=dur, sf=smooth_frac):
            v = np.zeros(4, dtype=float)
            v[i] = _bump_with_smooth_dot(t, t0, dur, A, sf)
            return v

        add["road_func"] = road_func
        add["road_func_dot"] = road_func_dot
        add["ax_func"] = lambda t: 0.0
        add["ay_func"] = lambda t: 0.0

    elif ttype == "кочка_диагональ":
        A = float(test.get("A_м", test.get("A", 0.0)) or 0.0)
        t0 = float(test.get("t0", 0.0) or 0.0)
        dur = float(test.get("dur", 0.0) or 0.0)
        v = float(test.get("speed_м_с", 0.0) or 0.0)
        ang = float(test.get("angle_град", 0.0) or 0.0)
        alpha = math.radians(ang)

        # Позиции колес относительно ЦМ (x вперед, y влево)
        xs = np.array([+L/2, +L/2, -L/2, -L/2], dtype=float)
        ys = np.array([+W/2, -W/2, +W/2, -W/2], dtype=float)

        if abs(v) < 1e-9:
            t_hits = np.full(4, t0, dtype=float)
        else:
            # Модель пересечения диагональной линии кочки (приближение):
            # t_hit = offset + (-(x + y*tan(alpha)) / v), offset выбрано так,
            # чтобы ЛП (0) имела t_hit = t0.
            tau = -(xs + ys * math.tan(alpha)) / v
            offset = t0 - tau[0]
            t_hits = offset + tau

        def road_func(t, A=A, dur=dur, t_hits=t_hits, sf=smooth_frac):
            return np.array([_bump_with_smooth(t, th, dur, A, sf) for th in t_hits], dtype=float)

        def road_func_dot(t, A=A, dur=dur, t_hits=t_hits, sf=smooth_frac):
            return np.array([_bump_with_smooth_dot(t, th, dur, A, sf) for th in t_hits], dtype=float)

        add["road_func"] = road_func
        add["road_func_dot"] = road_func_dot
        add["ax_func"] = lambda t: 0.0
        add["ay_func"] = lambda t: 0.0

    elif ttype == "комбо_крен_плюс_микро":
        ay = float(test.get("ay_м_с2", test.get("ay", 0.0)) or 0.0)
        t_step = float(test.get("t_step", 0.0) or 0.0)
        ramp = float(test.get("ramp_s", 0.05) or 0.05)
        A = float(test.get("A_м", test.get("A", 0.0)) or 0.0)
        f = float(test.get("f_Гц", test.get("f", 1.0)) or 1.0)
        w = 2.0 * math.pi * f
        add["ay_func"] = lambda t, ay=ay, t0=t_step, ramp=ramp: _step_with_ramp(t, t0, ay, ramp)
        add["ax_func"] = lambda t: 0.0
        add["road_func"] = lambda t, A=A, w=w: (A * math.sin(w * t)) * np.ones(4, dtype=float)
        add["road_func_dot"] = lambda t, A=A, w=w: (A * w * math.cos(w * t)) * np.ones(4, dtype=float)

    # Если не распознали — ничего не добавляем.
    return add


