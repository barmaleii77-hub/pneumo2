    W_axay = _cumtrapz(out['мощность_инерция_axay_Вт'], dt_)
    L_tire = _cumtrapz(out['мощность_диссипация_шины_Вт'], dt_)
    L_stop = _cumtrapz(out['мощность_диссипация_отбойники_Вт'], dt_)

