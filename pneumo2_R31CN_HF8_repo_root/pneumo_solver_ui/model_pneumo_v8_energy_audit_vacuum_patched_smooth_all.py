def smooth_abs(x, eps: float):
    """Гладкий |x| ≈ sqrt(x^2 + eps^2). eps имеет те же единицы, что и x."""
    return np.sqrt(x*x + (eps*eps))

def smooth_pos(x, eps: float):
    """Гладкий max(0,x) через 0.5*(x + sqrt(x^2+eps^2))."""
    return 0.5 * (x + smooth_abs(x, eps))

def smooth_pos_grad(x, eps: float):
    """Производная smooth_pos по x: гладкий аналог Heaviside(x) в [0;1]."""
    return 0.5 * (1.0 + x / smooth_abs(x, eps))

def smooth_max(x, lo, eps: float):
    """Гладкий max(x, lo)."""
    return lo + smooth_pos(x - lo, eps)

def smooth_min(x, hi, eps: float):
    """Гладкий min(x, hi)."""
    return hi - smooth_pos(hi - x, eps)

def smooth_clip_with_grad(x, lo, hi, eps: float):
    """Гладкий clip(x, lo, hi) и его производная dy/dx.
    Возвращает (y, dy_dx).
    """
    x1 = smooth_max(x, lo, eps)
    dx1_dx = smooth_pos_grad(x - lo, eps)
    y = smooth_min(x1, hi, eps)
    dy_dx1 = smooth_pos_grad(hi - x1, eps)
    dy_dx = dy_dx1 * dx1_dx
    return y, dy_dx

