        if smooth_spring:
            x_comp = smooth_max(x, 0.0, smooth_eps_pos_m)
            x_comp = smooth_min(x_comp, L_free, smooth_eps_pos_m)
        else:
            x_comp = np.clip(x, 0.0, L_free)
        U_spring = float(
            масштаб_пружины * float(np.sum(np.interp(x_comp, табл_пруж_ход_м_E, табл_пруж_энергия_Дж)))
        )

        # Гравитационная потенциальная энергия
        U_grav = float(m_body * g * float(state[0]) + m_w * g * float(np.sum(zw)))

        # Кинетическая энергия (согласно принятым DOF)
        z_dot_b = float(state[7])
        phi_dot_b = float(state[8])
        theta_dot_b = float(state[9])
        E_kin = float(
            0.5 * m_body * (z_dot_b ** 2)
            + 0.5 * I_roll * (phi_dot_b ** 2)
            + 0.5 * I_pitch * (theta_dot_b ** 2)
            + 0.5 * m_w * float(np.sum(zw_dot ** 2))
        )

        U_pot = float(U_grav + U_spring + U_tire + U_stop)
        E_mech = float(E_kin + U_pot)

        out['энергия_мех_кинетическая_Дж'][k] = E_kin
        out['энергия_мех_потенциальная_Дж'][k] = U_pot
        out['энергия_мех_Дж'][k] = E_mech
        out['энергия_гравитация_Дж'][k] = U_grav
        out['энергия_пружина_Дж'][k] = U_spring
        out['энергия_шины_Дж'][k] = U_tire
        out['энергия_отбойники_Дж'][k] = U_stop

        out['мощность_дороги_Вт'][k] = P_road
        out['мощность_газ_на_мех_Вт'][k] = P_pdv_gauge
        out['мощность_инерция_axay_Вт'][k] = P_axay
        out['мощность_диссипация_шины_Вт'][k] = D_tire
        out['мощность_диссипация_отбойники_Вт'][k] = D_stop

