# Газодинамика (дроссели)
# -------------------------
def mdot_orifice(p_up: float, p_dn: float, A: float, Cd: float = 0.8) -> float:
    """Массовый расход через отверстие из p_up в p_dn (p_up>p_dn), кг/с."""
    if A <= 0.0:
        return 0.0
    if p_up <= 0.0 or p_dn <= 0.0:
        return 0.0
    if p_up <= p_dn:
        return 0.0

    pr = p_dn / p_up
    pr_crit = (2.0 / (GAMMA + 1.0)) ** (GAMMA / (GAMMA - 1.0))

    if pr <= pr_crit:
        # захлопывание (choked)
        return Cd * A * p_up * math.sqrt(GAMMA / (R_AIR * T_AIR)) * (2.0 / (GAMMA + 1.0)) ** ((GAMMA + 1.0) / (2.0 * (GAMMA - 1.0)))
    else:
        term = pr ** (2.0 / GAMMA) - pr ** ((GAMMA + 1.0) / GAMMA)
        term = max(term, 0.0)
        return Cd * A * p_up * math.sqrt(2.0 * GAMMA / (R_AIR * T_AIR * (GAMMA - 1.0)) * term)

def mdot_orifice_signed(p1: float, p2: float, A: float, Cd: float = 0.8) -> float:
    """Двусторонний дроссель: знак расхода зависит от перепада."""
    if p1 > p2:
        return mdot_orifice(p1, p2, A, Cd)
    elif p2 > p1:
        return -mdot_orifice(p2, p1, A, Cd)
    else:
        return 0.0

def area_from_Qn(Qn_Nl_min: float,
                 p_up_abs: float = None,
                 dp: float = None,
                 Cd: float = 0.8,
                 P_N: float = P_N_DEFAULT,
                 T_N: float = T_N_DEFAULT) -> float:
    """Подбор эквивалентной площади A по номинальному расходу Qn (Nl/min).

    ВАЖНО про Nl/min:
    - 'Nl' (normal litre) обычно означает пересчёт объёмного расхода к нормальным условиям.
      Во многих инженерных источниках нормальные условия принимают как 0°C и 1.01325 bar abs.
    - Чтобы не было скрытой ошибки ~5–10% по проводимости (из‑за ρ_N), условия N вынесены в P_N/T_N.

    Здесь делаем:
        Qn [Nl/min] -> Qn [m³/s] при нормальных условиях -> ṁ_N = ρ_N * Qn
    и подбираем A так, чтобы ṁ_orifice(p_up, p_dn, A) совпал с ṁ_N в паспортной точке (6 bar, ΔP=1 bar).
    """
    # плотность воздуха при нормальных условиях
    rho_N = float(P_N) / (R_AIR * float(T_N))

    # нормальный объёмный расход (м³/с)
    Qn_m3_s = (Qn_Nl_min / 1000.0) / 60.0
    mdot_N = rho_N * Qn_m3_s

    # Паспортная точка Camozzi: подача 6 bar (обычно избыточное) и ΔP=1 bar.
    if p_up_abs is None:
        p_up_abs = P_ATM + 6e5
    if dp is None:
        dp = 1e5
    p_dn_abs = max(P_ATM, p_up_abs - dp)

    base = mdot_orifice(p_up_abs, p_dn_abs, 1.0, Cd)
    if base <= 0.0:
        return 1e-6
    return mdot_N / base

def A_from_diam_mm(d_mm: float) -> float:
    return math.pi * (d_mm * 1e-3 / 2.0) ** 2

