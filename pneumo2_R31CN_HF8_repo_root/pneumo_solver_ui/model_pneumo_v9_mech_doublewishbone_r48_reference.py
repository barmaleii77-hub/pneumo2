    A_eff, A_open, A_closed, _Ageom, C_eff, C_open, C_closed, _Qn_eff, _Qn_open, _Qn_closed, b_iso, m_iso = A_по_SCO("SCO 604-1/8", alpha_дроссель_выхлоп_Pmid)
