# ISO reference atmosphere for pneumatic flow characteristics (ANR)
#
# В стандарте ISO 6358 ссылка идёт на стандартную атмосферу (ISO 8778),
# где задаются T0=293.15 K, p0=100 kPa (1 bara) и ρ0≈1.185 kg/m^3 для воздуха.
# Эти условия используются для приведения Qn, C и др. паспортных параметров.
# ---------------------------------------------------------------------
P_ANR = 100000.0   # Па
T_ANR = 293.15     # К
RHO_ANR = 1.185    # кг/м^3 (воздух, ~65% RH в ISO 8778)

# -------------------------
# Газодинамика (дроссели)
# -------------------------
def mdot_orifice(p_up: float, p_dn: float, A: float, T_up: float = T_AIR, Cd: float = 0.8, gamma: float = GAMMA) -> float:
    """Массовый расход через отверстие из p_up в p_dn (p_up>p_dn), кг/с.

    Используем изоэнтропические формулы для истечения идеального газа с учётом захлёбывания.
    Температура берётся по upstream (стагнационная), как в типовых моделях газодинамики.
    """
    if A <= 0.0:
        return 0.0
    if p_up <= 0.0 or p_dn < 0.0:
        return 0.0
    if p_up <= p_dn:
        return 0.0

    T_up = float(T_up)
    if not np.isfinite(T_up) or T_up <= 1.0:
        T_up = T_AIR

    pr = p_dn / p_up
    pr_crit = (2.0 / (gamma + 1.0)) ** (gamma / (gamma - 1.0))

    if pr <= pr_crit:
        # захлопывание (choked)
        return Cd * A * p_up * math.sqrt(gamma / (R_AIR * T_up)) * (2.0 / (gamma + 1.0)) ** ((gamma + 1.0) / (2.0 * (gamma - 1.0)))
    else:
        term = pr ** (2.0 / gamma) - pr ** ((gamma + 1.0) / gamma)
        term = max(term, 0.0)
        return Cd * A * p_up * math.sqrt(2.0 * gamma / (R_AIR * T_up * (gamma - 1.0)) * term)

def mdot_orifice_signed(p1: float, p2: float, A: float, T1: float = T_AIR, T2: float = T_AIR, Cd: float = 0.8, gamma1: float = GAMMA, gamma2: float = GAMMA) -> float:
    """Двусторонний дроссель: знак расхода зависит от перепада.
    Upstream‑температура берётся со стороны большего давления.
    """
    if p1 > p2:
        return mdot_orifice(p1, p2, A, T1, Cd, gamma=gamma1)
    elif p2 > p1:
        return -mdot_orifice(p2, p1, A, T2, Cd, gamma=gamma2)
    else:
        return 0.0



# -------------------------
# ISO 6358 (C,b) расходная модель (опционально для пассивных дросселей/сопротивлений)
# -------------------------

# В ISO 6358 поток описывается параметрами:
#   C — sonic conductance (проводимость в режиме choked),
#   b — critical pressure ratio (граница choked/subsonic),
#   (опционально m и Δpc, но здесь используем базовую форму (C,b) как в типовых справочниках).
#
# Мы реализуем популярную инженерную формулу для Q (Nl/min) через C и b (см. источники в README):
#   Q = 600 * C * P1_abs(MPa) * sqrt(293/(273+t))
#   Q = 600 * C * P1_abs(MPa) * sqrt( 1 - ((pr-b)/(1-b))^2 ) * sqrt(293/(273+t))
#
# Внутри модели работаем в SI и переводим к массовому расходу через ρ_ANR.

ISO6358_B_DEFAULT = 0.5   # если b неизвестен, для многих компонентов фиксированного прохода b≈0.5
ISO6358_M_DEFAULT = 0.5   # ISO 6358-1: для фиксированного прохода m обычно около 0.5
ISO6358_BETA_LAM_DEFAULT = 0.999  # давление почти без перепада: численное сглаживание (типично 0.995..0.999)



def iso6358_phi(pr: float, b: float, m: float = ISO6358_M_DEFAULT, beta_lam: float = ISO6358_BETA_LAM_DEFAULT) -> float:
    """Коэффициент расхода φ(pr) для ISO 6358 (обобщённая форма с подгонкой субзвуковой области).

    pr = p_dn / p_up (абсолютные давления).
    b  = critical pressure ratio.
    m  = subsonic index (экспоненциальный индекс для субзвуковой ветви).
    beta_lam — коэффициент численного сглаживания при pr→1 (ламинаризация), 0.995..0.999.

    Интерпретация (практический инженерный вариант):
    - при pr <= b: choked => φ=1
    - при pr >  b: subsonic => φ = (1 - x^2)^m, x=(pr-b)/(1-b)
      При m=0.5 получаем классическую «эллиптическую» аппроксимацию, используемую в справочниках.

    Далее вводится линейная «ламинаризация» на участке pr∈[beta_lam,1],
    чтобы избежать плохой численной обусловленности около нулевого расхода.
    """
    if (not np.isfinite(pr)):
        return 0.0
    if pr <= 0.0:
        # pr<=0 трактуем как сильный перепад (вакуум): режим захлёбывания
        return 1.0
    if pr >= 1.0:
        return 0.0
    if not np.isfinite(b):
        b = ISO6358_B_DEFAULT
    if not np.isfinite(m):
        m = ISO6358_M_DEFAULT
    if not np.isfinite(beta_lam):
        beta_lam = ISO6358_BETA_LAM_DEFAULT

    b = float(np.clip(b, 1e-6, 0.999999))
    m = float(max(0.05, min(5.0, m)))
    beta_lam = float(np.clip(beta_lam, 0.95, 0.999999))

    # базовая ветвь
    if pr <= b:
        phi = 1.0
    else:
        x = (pr - b) / (1.0 - b)
        x = float(np.clip(x, 0.0, 1.0))
        base = max(0.0, 1.0 - x * x)
        phi = base ** m

    # численное сглаживание при pr→1 (малая Δp)
    if pr >= beta_lam:
        # значение φ на границе сглаживания
        if beta_lam <= b:
            phi_lam = 1.0
        else:
            x_lam = (beta_lam - b) / (1.0 - b)
            x_lam = float(np.clip(x_lam, 0.0, 1.0))
            base_lam = max(0.0, 1.0 - x_lam * x_lam)
            phi_lam = base_lam ** m
        # линейно к нулю при pr->1
        k = (1.0 - pr) / (1.0 - beta_lam)
        phi = max(0.0, min(1.0, phi_lam * k))

    return float(max(0.0, min(1.0, phi)))


def C_iso_from_area_SMC(A_m2: float) -> float:
    """Приближённая конверсия A -> C по инженерной связи S=5C (SMC).

    S — effective area [mm^2], C — [dm^3/(s·bar)].
    В SI это: C_SI [m^3/(s·Pa)] = (S/5) * 1e-8.

    Важно: это *приближение* (эффективная площадь уже включает Cd и геометрию).
    """
    A_m2 = float(A_m2)
    if A_m2 <= 0.0 or (not np.isfinite(A_m2)):
        return 0.0
    S_mm2 = A_m2 * 1e6
    C_dm3_s_bar = S_mm2 / 5.0
    return C_dm3_s_bar * 1e-8


def Qn_from_C_iso(C_m3_s_Pa: float, p_up_abs: float = None, dp: float = None, b: float = ISO6358_B_DEFAULT, m: float = ISO6358_M_DEFAULT, beta_lam: float = ISO6358_BETA_LAM_DEFAULT, T_up: float = T_AIR) -> float:
    """Оценка Qn (Nl/min, ANR) по параметру C (ISO 6358) при заданной «паспортной» точке.

    По умолчанию: p_up=6 bar(g), Δp=1 bar (т.е. p_up_abs=7 bar(a), p_dn_abs=6 bar(a)).

    Формула реализует объёмный расход в нормальных условиях (ANR), затем переводит в Nl/min.
    """
    if p_up_abs is None:
        p_up_abs = P_ATM + 6e5
    if dp is None:
        dp = 1e5
    p_dn_abs = max(P_ATM, float(p_up_abs) - float(dp))

    p_up_abs = float(p_up_abs)
    if p_up_abs <= 0.0 or C_m3_s_Pa <= 0.0:
        return 0.0

    pr = p_dn_abs / p_up_abs
    phi = iso6358_phi(pr, b, m=m, beta_lam=beta_lam)
    T_up = float(T_up) if (np.isfinite(T_up) and T_up > 1.0) else T_AIR

    qn_m3_s = float(C_m3_s_Pa) * p_up_abs * phi * math.sqrt(T_ANR / T_up)
    return qn_m3_s * 60.0 * 1000.0


def C_from_Qn_iso(Qn_Nl_min: float, p_up_abs: float = None, dp: float = None, b: float = ISO6358_B_DEFAULT, m: float = ISO6358_M_DEFAULT, beta_lam: float = ISO6358_BETA_LAM_DEFAULT, T_up: float = T_AIR) -> float:
    """Подбор C (ISO 6358, SI) по паспортному Qn в одной измерительной точке.

    Это полезно, когда известен только Qn (Nl/min) и предполагается b.
    В отличие от area_from_Qn(), здесь используется ISO 6358‑совместимая аппроксимация расхода.
    """
    Qn_m3_s = (float(Qn_Nl_min) / 1000.0) / 60.0

    if p_up_abs is None:
        p_up_abs = P_ATM + 6e5
    if dp is None:
        dp = 1e5
    p_dn_abs = max(P_ATM, float(p_up_abs) - float(dp))

    p_up_abs = float(p_up_abs)
    if p_up_abs <= 0.0:
        return 0.0

    pr = p_dn_abs / p_up_abs
    phi = iso6358_phi(pr, b, m=m, beta_lam=beta_lam)
    if phi <= 0.0:
        return 0.0

    T_up = float(T_up) if (np.isfinite(T_up) and T_up > 1.0) else T_AIR
    return Qn_m3_s / (p_up_abs * phi * math.sqrt(T_ANR / T_up))


def mdot_iso6358(p_up: float, p_dn: float, C_m3_s_Pa: float, b: float = ISO6358_B_DEFAULT, m: float = ISO6358_M_DEFAULT, beta_lam: float = ISO6358_BETA_LAM_DEFAULT, T_up: float = T_AIR) -> float:
    """Массовый расход (кг/с) через элемент с параметрами ISO 6358 (C,b).

    p_up>p_dn (абсолютные давления).
    Возвращает ṁ>=0.

    Примечание: это эмпирическая/паспортная модель. Она хорошо подходит для
    клапанов/соединений/дросселей с известными C,b, но ISO 6358 напрямую
    НЕ применим к регуляторам (внутренняя обратная связь) и к элементам,
    которые обменивают энергию с газом (цилиндры, аккумуляторы), см. ISO 6358‑1.
    """
    C_m3_s_Pa = float(C_m3_s_Pa)
    if C_m3_s_Pa <= 0.0 or (not np.isfinite(C_m3_s_Pa)):
        return 0.0
    if p_up <= p_dn or p_up <= 0.0 or p_dn < 0.0:
        return 0.0

    T_up = float(T_up) if (np.isfinite(T_up) and T_up > 1.0) else T_AIR

    pr = p_dn / p_up
    phi = iso6358_phi(pr, b, m=m, beta_lam=beta_lam)
    qn_m3_s = C_m3_s_Pa * p_up * phi * math.sqrt(T_ANR / T_up)
    return RHO_ANR * qn_m3_s


def mdot_iso6358_signed(p1: float, p2: float, C_m3_s_Pa: float, b: float = ISO6358_B_DEFAULT, m: float = ISO6358_M_DEFAULT, beta_lam: float = ISO6358_BETA_LAM_DEFAULT, T1: float = T_AIR, T2: float = T_AIR) -> float:
    """Двусторонний элемент ISO 6358: знак расхода определяется перепадом давления."""
    if p1 > p2:
        return mdot_iso6358(p1, p2, C_m3_s_Pa, b=b, m=m, beta_lam=beta_lam, T_up=T1)
    elif p2 > p1:
        return -mdot_iso6358(p2, p1, C_m3_s_Pa, b=b, m=m, beta_lam=beta_lam, T_up=T2)
    else:
        return 0.0


def mdot_iso6358_check_signed(p1: float, p2: float, C_m3_s_Pa: float, b: float = ISO6358_B_DEFAULT,
                              m: float = ISO6358_M_DEFAULT, beta_lam: float = ISO6358_BETA_LAM_DEFAULT,
                              dp_crack: float = 0.0, dp_width: float = 0.0,
                              T1: float = T_AIR, T2: float = T_AIR) -> float:
    """ISO 6358 поток через элемент с направленностью (check / one-way).

    Инженерная трактовка Δpc (dp_crack) по ISO 6358:
    - До «cracking pressure» расход ~0 (клапан закрыт).
    - После открытия часть перепада «съедается» пружиной/механикой клапана.
      Поэтому для расчёта расхода используем *эффективное* входное давление:
          p_up_eff = p_up - Δpc,
      что эквивалентно уменьшению доступного для течения перепада: Δp_eff = Δp - Δpc.

    Для численной устойчивости делаем плавное включение в окрестности dp_crack
    шириной dp_width (Па): Δp_eff сглаживаем через smooth_open_fraction().

    Возвращает ṁ (кг/с) в направлении 1→2 (только если p1>p2).
    """
    if p1 <= p2:
        return 0.0
    dp = p1 - p2
    # Δp_eff = max(0, Δp - Δpc), со сглаживанием в окрестности порога
    dp_eff = max(0.0, dp - float(dp_crack))
    if dp_eff <= 0.0:
        return 0.0
    if dp_width and dp_width > 0.0:
        dp_eff *= smooth_open_fraction(dp, float(dp_crack), float(dp_width))
    p1_eff = p2 + dp_eff  # = p1 - Δpc (когда сглаживание≈1)
    if p1_eff <= p2:
        return 0.0
    return mdot_iso6358(p1_eff, p2, C_m3_s_Pa, b=b, m=m, beta_lam=beta_lam, T_up=T1)



# -------------------------
# Плавные функции (для открытия клапанов без «дребезга» и разрывов)
