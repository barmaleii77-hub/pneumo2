def smoothstep01(x: float) -> float:
    """Плавная функция 0..1 (C1 smoothstep)."""
    if x <= 0.0:
        return 0.0
    if x >= 1.0:
        return 1.0
    return x * x * (3.0 - 2.0 * x)

def smooth_open_fraction(dp: float, dp0: float, dp_width: float) -> float:
    """Доля открытия 0..1 как функция перепада dp=pu-pdn.

    dp0 — «порог» (например cracking pressure),
    dp_width — ширина перехода (Па). Если dp_width<=0, переход ступенчатый.
    """
    if dp_width <= 0.0:
        return 1.0 if dp > dp0 else 0.0
    return smoothstep01((dp - dp0) / dp_width)
def area_from_Qn(Qn_Nl_min: float, p_up_abs: float = None, dp: float = None, Cd: float = 0.8) -> float:
    """Подбор эквивалентной площади A по номинальному расходу Qn (Nl/min)."""
    # Важно: Qn (Nl/min) в каталогах пневматики обычно отнесён к стандартной атмосфере (ANR).
    # В ISO 6358-1 это p0=100 kPa, T0=293.15 K и ρ0=1.185 kg/m^3.
    rho_N = RHO_ANR
    Qn_m3_s = (Qn_Nl_min / 1000.0) / 60.0
    mdot_N = rho_N * Qn_m3_s

    if p_up_abs is None:
        p_up_abs = P_ATM + 6e5
    if dp is None:
        dp = 1e5
    p_dn_abs = max(P_ATM, p_up_abs - dp)

    base = mdot_orifice(p_up_abs, p_dn_abs, 1.0, T_AIR, Cd)
    if base <= 0.0:
        return 1e-6
    return mdot_N / base


def Qn_from_area(A: float, p_up_abs: float = None, dp: float = None, Cd: float = 0.8) -> float:
    """Обратная функция: оценка Qn (Nl/min, ANR) для заданной площади A.

    Используется тот же эталон ANR (ρ0=1.185 kg/m^3) и те же условия измерения
    (по умолчанию p_up=6 bar(g) и Δp=1 bar).
    """
    if p_up_abs is None:
        p_up_abs = P_ATM + 6e5
    if dp is None:
        dp = 1e5
    p_dn_abs = max(P_ATM, p_up_abs - dp)
    mdot = mdot_orifice(p_up_abs, p_dn_abs, float(A), T_AIR, Cd)
    Qn_m3_s = mdot / RHO_ANR
    return Qn_m3_s * 60.0 * 1000.0

def A_from_diam_mm(d_mm: float) -> float:
    return math.pi * (d_mm * 1e-3 / 2.0) ** 2

