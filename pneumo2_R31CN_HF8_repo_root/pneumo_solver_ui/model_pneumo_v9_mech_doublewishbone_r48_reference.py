    add_node('Магистраль_ЛП2_ПЗ2', 'fixed', V_line)
    add_node('Магистраль_ПП2_ЛЗ2', 'fixed', V_line)

    diag_pairs = [('ЛП', 'ПЗ', 'Магистраль_ЛП2_ПЗ2'), ('ПП', 'ЛЗ', 'Магистраль_ПП2_ЛЗ2')]
