    add_node('узел_после_ОК_Pmid', 'fixed', V_line)
    add_node('узел_после_рег_Pmin_сброс', 'fixed', V_line)
    add_node('узел_после_ОК_Pmin', 'fixed', V_line)
    add_node('узел_после_рег_заряд_аккумулятора', 'fixed', V_line)

