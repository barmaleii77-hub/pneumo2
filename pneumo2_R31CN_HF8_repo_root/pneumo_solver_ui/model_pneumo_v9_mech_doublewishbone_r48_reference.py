def normalize_component_passport(data: dict) -> dict:
    """Нормализовать формат component_passport.json.

    Поддерживаем два формата:

    1) "старый" (dict семейств):
       {
         "meta": {...},
         "VNR": { "VNR-238-3/8": {"iso": {...}}, ... },
         "SCO": { "SCO 606-1/4": {"iso": {...}}, ... }
       }

    2) "новый/расширенный" (список components), который встречается в проекте:
       {
         "meta": {...},
         "components": [
             {"family":"VNR","id":"VNR-...", "datasheet":{...}, "iso6358":{...}},
             {"family":"SCO","id":"SCO ...", "datasheet":{...}, "iso6358":{...}},
             ...
         ]
       }

    Возвращает dict в "старом" виде (dict семейств), чтобы остальная модель могла
    использовать passport_get().

    Дополнительно:
    - Если встречаются семейства кроме VNR/SCO (например MC/VMR/SIL), мы тоже
      переносим их в выходной dict, чтобы можно было делать passport_get(passport,'MC',...)
      и использовать ISO6358-параметры для регуляторов/клапанов/глушителей.
    """

    # Уже старый формат
    if isinstance(data.get('VNR', None), dict) or isinstance(data.get('SCO', None), dict):
        return data

    comps = data.get('components', None)
    if not isinstance(comps, list):
        return data

    out: dict = {
        'meta': data.get('meta', {}),
    }

    def _f(x, default=None):
        try:
            if x is None:
                return default
            return float(x)
        except Exception:
            return default

    def _fam_key(raw: str) -> str:
        fu = str(raw or '').strip().upper()
        if fu in ('SILENCER', 'SIL', 'MUFFLER', 'SOUND', 'SOUND_DAMPER', 'SOUND_DAMPENER', 'SILENCING_BUSH'):
            return 'SIL'
        return fu

    def _ensure_family(fk: str):
        if fk and (fk not in out):
            out[fk] = {}

    for comp in comps:
        if not isinstance(comp, dict):
            continue

        fam_key = _fam_key(comp.get('family', ''))
        _ensure_family(fam_key)

        # code: prefer in_code.camozzi_код_string, else id
        code = None
        try:
            code = (comp.get('in_code', {}) or {}).get('camozzi_код_string', None)
        except Exception:
            code = None
        if not code:
            code = comp.get('id', None)
        if not code:
            continue
        code = str(code)

        ds = comp.get('datasheet', {}) if isinstance(comp.get('datasheet', {}), dict) else {}
        iso = comp.get('iso6358', {}) if isinstance(comp.get('iso6358', {}), dict) else {}

        if fam_key == 'VNR':
            Qn = _f(ds.get('Qn_Nl_min_at_6bar_dp1bar', ds.get('Qn_Nl_min', None)), default=None)
            # dp_open_bar: предпочтительно из iso6358.delta_pc_bar, иначе из datasheet.min_operating_pressure_bar
            dp_bar = _f(iso.get('delta_pc_bar', None), default=None)
            if dp_bar is None:
                dp_bar = _f(ds.get('min_operating_pressure_bar', ds.get('dp_open_bar', 0.0)), default=0.0)

            C_m3 = _f(iso.get('C_m3_s_Pa', None), default=None)
            C_dm = _f(iso.get('C_dm3_s_bar', None), default=None)
            if (C_m3 is None) and (C_dm is not None):
                C_m3 = C_dm * 1e-8
            if (C_dm is None) and (C_m3 is not None):
                C_dm = C_m3 * 1e8

            b = _f(iso.get('b', None), default=None)
            m = _f(iso.get('m', None), default=None)

            out['VNR'][code] = {
                'Qn_Nl_min': Qn if Qn is not None else 0.0,
                'dp_open_bar': dp_bar if dp_bar is not None else 0.0,
                'iso': {
                    'C_m3_s_Pa': C_m3,
                    'C_dm3_s_bar': C_dm,
                    'b': b,
                    'm': m,
                }
            }
            continue

        if fam_key == 'SCO':
            Qn_open = _f(ds.get('Qn_open_Nl_min_at_6bar_dp1bar', ds.get('Qn_open_Nl_min', None)), default=None)
            Qn_closed = _f(ds.get('Qn_closed_Nl_min_at_6bar_dp1bar', ds.get('Qn_closed_Nl_min', None)), default=None)
            dmax = _f(ds.get('d_max_mm', ds.get('d_max_мм', None)), default=None)

            C_open_m3 = _f(iso.get('C_open_m3_s_Pa', None), default=None)
            C_closed_m3 = _f(iso.get('C_closed_m3_s_Pa', None), default=None)
            C_open_dm = _f(iso.get('C_open_dm3_s_bar', None), default=None)
            C_closed_dm = _f(iso.get('C_closed_dm3_s_bar', None), default=None)

            if (C_open_m3 is None) and (C_open_dm is not None):
                C_open_m3 = C_open_dm * 1e-8
            if (C_open_dm is None) and (C_open_m3 is not None):
                C_open_dm = C_open_m3 * 1e8

            if (C_closed_m3 is None) and (C_closed_dm is not None):
                C_closed_m3 = C_closed_dm * 1e-8
            if (C_closed_dm is None) and (C_closed_m3 is not None):
                C_closed_dm = C_closed_m3 * 1e8

            b = _f(iso.get('b', None), default=None)
            m = _f(iso.get('m', None), default=None)

            out['SCO'][code] = {
                'd_max_mm': dmax if dmax is not None else 0.0,
                'Qn_open_Nl_min': Qn_open if Qn_open is not None else 0.0,
                'Qn_closed_Nl_min': Qn_closed if Qn_closed is not None else 0.0,
                'iso': {
                    'C_open_m3_s_Pa': C_open_m3,
                    'C_closed_m3_s_Pa': C_closed_m3,
                    'C_open_dm3_s_bar': C_open_dm,
                    'C_closed_dm3_s_bar': C_closed_dm,
                    'b': b,
                    'm': m,
                }
            }
            continue

        # Generic handler for non‑VNR/SCO families (MC, VMR, SIL, ...)
        Qn = _f(ds.get('Qn_Nl_min_at_6bar_dp1bar', ds.get('Qn_Nl_min', None)), default=None)
        delta_pc_bar = _f(iso.get('delta_pc_bar', None), default=None)
        if delta_pc_bar is None:
            delta_pc_bar = _f(ds.get('min_operating_pressure_bar', 0.0), default=0.0)

        # Prefer scalar C; if only an "open" value exists (needle valves), use it.
        C_m3 = _f(iso.get('C_m3_s_Pa', iso.get('C_open_m3_s_Pa', None)), default=None)
        C_dm = _f(iso.get('C_dm3_s_bar', iso.get('C_open_dm3_s_bar', None)), default=None)
        if (C_m3 is None) and (C_dm is not None):
            C_m3 = C_dm * 1e-8
        if (C_dm is None) and (C_m3 is not None):
            C_dm = C_m3 * 1e8

        b = _f(iso.get('b', None), default=None)
        m = _f(iso.get('m', None), default=None)

        out[fam_key][code] = {
            'Qn_Nl_min': Qn if Qn is not None else 0.0,
            'dp_open_bar': delta_pc_bar if delta_pc_bar is not None else 0.0,
            'iso': {
                'C_m3_s_Pa': C_m3,
                'C_dm3_s_bar': C_dm,
                'b': b,
                'm': m,
                'delta_pc_bar': delta_pc_bar if delta_pc_bar is not None else 0.0,
            }
        }

    return out

def load_component_passport(passport_path: str | None) -> dict | None:
    """Загрузить JSON‑паспорт компонентов.

    Путь может быть относительным (относительно папки, где лежит модель).
    Результат кэшируется на уровне модуля.

    Формат ожидается такой же, как у generate_component_passport.py:
      - верхний уровень: meta, VNR, SCO, ...
      - для каждого компонента: iso: {C_..., b, m, ...}

    Возвращает dict или None, если файл не найден/битый.
    """
    if not passport_path:
        return None
    try:
        p = Path(str(passport_path))
        if not p.is_absolute():
            p = (HERE / p).resolve()
    except Exception:
        return None

    key = str(p)
    if key in _COMPONENT_PASSPORT_CACHE:
        return _COMPONENT_PASSPORT_CACHE[key]

    if not p.exists():
        _COMPONENT_PASSPORT_CACHE[key] = None
        return None

    try:
        data = json.loads(p.read_text(encoding="utf-8"))
        if isinstance(data, dict):
            data = normalize_component_passport(data)
        else:
            data = None
    except Exception:
        data = None

    _COMPONENT_PASSPORT_CACHE[key] = data
    return data


def passport_get(passport: dict | None, family: str, code: str) -> dict | None:
    """Безопасно достать запись из паспорта."""
    if not passport or (not isinstance(passport, dict)):
        return None
    fam = passport.get(family)
    if not isinstance(fam, dict):
        return None
    ent = fam.get(code)
    if not isinstance(ent, dict):
        return None
    return ent

