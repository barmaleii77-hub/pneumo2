    stroke_C2_corner = np.array([L_stroke_C2_front, L_stroke_C2_front, L_stroke_C2_rear, L_stroke_C2_rear], dtype=float)
    s0_C1_corner = 0.5 * stroke_C1_corner
    s0_C2_corner = 0.5 * stroke_C2_corner

