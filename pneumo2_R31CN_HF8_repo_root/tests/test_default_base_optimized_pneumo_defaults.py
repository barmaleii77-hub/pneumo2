from __future__ import annotations

import json
import math
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
DEFAULT_BASE = ROOT / "pneumo_solver_ui" / "default_base.json"
DEFAULT_RANGES = ROOT / "pneumo_solver_ui" / "default_ranges.json"


def _base() -> dict:
    return json.loads(DEFAULT_BASE.read_text(encoding="utf-8"))


def _ranges() -> dict:
    return json.loads(DEFAULT_RANGES.read_text(encoding="utf-8"))


def test_default_base_uses_contract_safe_optimized_pneumo_startup_defaults() -> None:
    base = _base()

    expected = {
        "давление_Pmid_сброс": 851325.0,
        "давление_Pmin_питание_Ресивер2": 505300.0,
        "давление_Pmin_сброс": 601325.0,
        "давление_Pзаряд_аккумулятора_из_Ресивер3": 911325.0,
        "диаметр_поршня_Ц1": 0.045,
        "низ_Ц1_перед_доля_рычага": 0.82,
        "низ_Ц1_зад_доля_рычага": 0.82,
        "низ_Ц2_перед_доля_рычага": 0.74,
        "низ_Ц2_зад_доля_рычага": 0.74,
        "объём_ресивера_2": 0.002,
        "объём_ресивера_3": 0.003,
        "открытие_дросселя_Ц2_CAP_в_ROD": 0.26,
        "открытие_дросселя_Ц2_ROD_в_CAP": 0.14,
        "пружина_масштаб": 0.14,
        "ход_штока": 0.32,
        "ход_штока_Ц1_перед_м": 0.32,
        "ход_штока_Ц1_зад_м": 0.32,
        "ход_штока_Ц2_перед_м": 0.32,
        "ход_штока_Ц2_зад_м": 0.32,
        "верх_Ц1_перед_x_относительно_оси_ступицы_м": 0.05,
        "верх_Ц1_зад_x_относительно_оси_ступицы_м": 0.05,
        "верх_Ц2_перед_x_относительно_оси_ступицы_м": -0.06,
        "верх_Ц2_зад_x_относительно_оси_ступицы_м": -0.06,
        "верх_Ц1_перед_z_относительно_рамы_м": 0.52,
        "верх_Ц1_зад_z_относительно_рамы_м": 0.52,
        "верх_Ц2_перед_z_относительно_рамы_м": 0.52,
        "верх_Ц2_зад_z_относительно_рамы_м": 0.52,
    }
    for key, value in expected.items():
        actual = float(base[key])
        assert math.isclose(actual, float(value), rel_tol=0.0, abs_tol=1e-12), key

    assert base["стабилизатор_вкл"] is False
    assert float(base["начальное_давление_Ресивер3"]) < float(base["давление_Pmin_сброс"])
    assert float(base["давление_Pmin_сброс"]) < float(base["давление_Pmid_сброс"])
    assert float(base["давление_Pmid_сброс"]) < float(base["давление_Pзаряд_аккумулятора_из_Ресивер3"])
    for key in (
        "верх_Ц1_перед_z_относительно_рамы_м",
        "верх_Ц1_зад_z_относительно_рамы_м",
        "верх_Ц2_перед_z_относительно_рамы_м",
        "верх_Ц2_зад_z_относительно_рамы_м",
    ):
        assert not math.isclose(float(base[key]), float(base["высота_рамы"]), rel_tol=0.0, abs_tol=1e-12), key


def test_default_ranges_cover_optimized_pneumo_startup_defaults() -> None:
    base = _base()
    ranges = _ranges()

    tuned_keys = (
        "давление_Pmid_сброс",
        "давление_Pmin_питание_Ресивер2",
        "давление_Pmin_сброс",
        "давление_Pзаряд_аккумулятора_из_Ресивер3",
        "диаметр_поршня_Ц1",
        "низ_Ц1_перед_доля_рычага",
        "низ_Ц1_зад_доля_рычага",
        "низ_Ц2_перед_доля_рычага",
        "низ_Ц2_зад_доля_рычага",
        "объём_ресивера_2",
        "объём_ресивера_3",
        "открытие_дросселя_Ц2_CAP_в_ROD",
        "открытие_дросселя_Ц2_ROD_в_CAP",
        "пружина_масштаб",
        "ход_штока_Ц1_перед_м",
        "ход_штока_Ц1_зад_м",
        "ход_штока_Ц2_перед_м",
        "ход_штока_Ц2_зад_м",
        "верх_Ц1_перед_x_относительно_оси_ступицы_м",
        "верх_Ц1_зад_x_относительно_оси_ступицы_м",
        "верх_Ц2_перед_x_относительно_оси_ступицы_м",
        "верх_Ц2_зад_x_относительно_оси_ступицы_м",
        "верх_Ц1_перед_z_относительно_рамы_м",
        "верх_Ц1_зад_z_относительно_рамы_м",
        "верх_Ц2_перед_z_относительно_рамы_м",
        "верх_Ц2_зад_z_относительно_рамы_м",
    )

    for key in tuned_keys:
        lo, hi = ranges[key]
        assert float(lo) <= float(base[key]) <= float(hi), key

    assert float(ranges["пружина_масштаб"][0]) <= 0.14
    for key in (
        "верх_Ц1_перед_z_относительно_рамы_м",
        "верх_Ц1_зад_z_относительно_рамы_м",
        "верх_Ц2_перед_z_относительно_рамы_м",
        "верх_Ц2_зад_z_относительно_рамы_м",
    ):
        assert float(ranges[key][1]) > float(base["высота_рамы"]), key
